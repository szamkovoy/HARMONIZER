/**
 * Единая точка правды для порогов HRV-практики (RMSSD, индекс Баевского).
 * Используется в `metrics.ts` и `finger-analysis.ts`, чтобы не расходились границы RR и числа ударов.
 */

/** Жёсткие границы длительности RR (мс) для пар, участвующих в RMSSD/Баевском после отбора. */
export const HRV_RR_HARD_MIN_MS = 300;
export const HRV_RR_HARD_MAX_MS = 2000;

/**
 * Минимум валидных ударов в накопителе HRV, при котором `computePracticeHrvMetrics` начинает отдавать ненулевой тир.
 * Совпадает с порогом в `FingerSignalAnalyzer` для показа RMSSD и захвата сессии.
 */
export const HRV_MIN_VALID_BEATS_FOR_METRICS = 30;

/**
 * Число первых валидных ударов (по ординалам), задающих префикс для «начального» сегмента при длинной практике.
 * Даёт до 89 интервалов RR между ударами 0…89 (конец на 90-м ударе).
 */
export const HRV_PREFIX_BEATS_FOR_SEGMENT = 90;

/** Хвост из K последних валидных ударов для финального сегмента (тир 120–179). */
export const HRV_TAIL_BEATS_FINAL_MID = 60;

/** Хвост из K последних валидных ударов для финального сегмента (тир 180+). */
export const HRV_TAIL_BEATS_FINAL_LONG = 90;

/** После стольких валидных ударов однократно фиксируются «начальные» RMSSD/стресс (латч). */
export const HRV_LATCH_INITIAL_AFTER_BEATS = 90;

/** Верхняя граница накопителя для тиров начало/конец в UI и экспорте. */
export const HRV_TIER_MAX_BEATS = 180;

/**
 * Ориентир для будущей интеграции: один замер RMSSD по N валидным ударам (другие модули проекта).
 * Текущий движок не имеет отдельного тира «ровно 120»: при 120 ударов действует тир `beats_120_179`
 * (начало по первым 90, конец по последним 60 ударов) или при желании можно читать агрегат по префиксу из `computePracticeHrvMetrics`.
 */
export const HRV_INTEGRATION_SINGLE_MEASURE_BEATS = 120;
